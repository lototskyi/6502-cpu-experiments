code = bytearray([
	0xa9, 0xff,			# lda #$ff
	0x8d, 0x03, 0x80,	# sta $8003
	
	0xa9, 0x55,			# lda #$55
	0x8d, 0x01, 0x80,	# sta $8001
	
	0xa9, 0xaa,			# lda #$aa
	0x8d, 0x01, 0x80,	# sta $8001
	
	0x4c, 0x05, 0xc0	# jmp $c005
])

rom = bytearray([0x00] * 0x4000) + code + bytearray([0xea] * (32768 - len(code)))

rom[0x7ffc] = 0x00
rom[0x7ffd] = 0xc0

with open("rom.bin", "wb") as out_file:
	out_file.write(rom);